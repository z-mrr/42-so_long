#include <stdlib.h>

char	*ft_strdup(const char *s)
{
	char	*str;
	int		i;
	int		len;

	len = 0;
	while (s[len])
		len++;
	str = (char *)malloc(sizeof(char) * len + 1);
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
